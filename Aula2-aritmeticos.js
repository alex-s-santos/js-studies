let num = 2;

console.log('Operadores Aritiméticos');
console.log(num + 2); //Soma
console.log(num - 2); //Subtração
console.log(num * 2); //Multiplicação
console.log(num / 2); //Divisão
console.log(num % 2); //Módulo

console.log('Exponenciação');
console.log(num ** 2);

console.log('Aritiméticos com atribuição');
console.log(num += 1);
console.log(num -= 1);
console.log(num *= 1);
console.log(num /= 1);
console.log(num %= 1);

console.log('Incremento e Decremento');

//Incrementa depois de executar
num++;
console.log(num);
num--;
console.log(num);

//Incrementa antes 
console.log(++num);
console.log(--num);